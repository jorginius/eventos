CREATE TABLE if NOT EXISTS SOURCES
(
    ID
    BIGINT
    PRIMARY
    KEY
    AUTO_INCREMENT,
    SOURCE
    VARCHAR
(
    255
) NOT NULL,
    VERSION BIGINT NOT NULL DEFAULT 0
    );

CREATE TABLE if NOT EXISTS EVENTS
(
    ID
    BIGINT
    PRIMARY
    KEY
    AUTO_INCREMENT,
    SOURCE_ID
    BIGINT
    NOT
    NULL,
    STAMP
    TIMESTAMP
    DEFAULT
    CURRENT_TIMESTAMP,
    MEASURE
    BIGINT
    NOT
    NULL,
    VERSION
    BIGINT
    NOT
    NULL
    DEFAULT
    0,
    FOREIGN
    KEY
(
    SOURCE_ID
) REFERENCES SOURCES
(
    ID
)
    );
